package socket;

public class Chat {

    int contacto;
    String conve = "";

    public Chat() {
    }

    public int getContacto() {
        return contacto;
    }

    public void setContacto(int contacto) {
        this.contacto = contacto;
    }

    public String getConve() {
        return conve;
    }

    public void setConve(String conve) {
        this.conve = conve;
    }
}